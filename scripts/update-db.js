async function main() {
  console.log("[Update] 开始抓取数据...");
  const start = Date.now();

  const [ripeData, apnicData, lacnicData] = await Promise.all([
    fetchRIPE(),
    fetchAPNIC(),
    fetchLACNIC()
  ]);

  console.log(`[Update] RIPE:${Object.keys(ripeData).length} APNIC:${Object.keys(apnicData).length} LACNIC:${Object.keys(lacnicData).length}`);

  const merged = mergeAll(cleanSeed(SEED), ripeData, apnicData, lacnicData);
  const limited = limitAndShuffle(merged);

  const payload = {
    ips: limited,
    updated_at: new Date().toISOString(),
    source: "ripe+apnic+lacnic",
    country_count: Object.keys(limited).length,
  };

  // 确保输出到仓库根目录的 data 文件夹
  const outputDir = path.join(process.cwd(), 'data');
  fs.mkdirSync(outputDir, { recursive: true });
  const outputPath = path.join(outputDir, 'ip-database.json');
  fs.writeFileSync(outputPath, JSON.stringify(payload, null, 2), 'utf8');

  const elapsed = ((Date.now() - start) / 1000).toFixed(1);
  console.log(`[Update] 完成！${payload.country_count} 个国家，耗时 ${elapsed}s`);
  console.log(`[Update] 已写入 ${outputPath}`);
}

main().catch(e => {
  console.error(e);
  process.exit(1);
});
