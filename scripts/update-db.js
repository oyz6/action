function isPublicIP(ip) {
  if (!ip) return false;
  const p = ip.split(".").map(Number);
  if (p.length !== 4 || p.some(x => isNaN(x) || x < 0 || x > 255)) return false;
  const [a, b, c] = p;
  if (a === 0)                             return false;
  if (a === 10)                            return false;
  if (a === 100 && b >= 64 && b <= 127)   return false;
  if (a === 127)                           return false;
  if (a === 169 && b === 254)              return false;
  if (a === 172 && b >= 16 && b <= 31)    return false;
  if (a === 192 && b === 0 && c === 2)     return false;
  if (a === 192 && b === 168)              return false;
  if (a === 198 && (b === 18 || b === 19)) return false;
  if (a === 198 && b === 51 && c === 100)  return false;
  if (a === 203 && b === 0 && c === 113)   return false;
  if (a >= 224)                            return false;
  return true;
}

function addOffset(ip, offset) {
  if (!ip) return null;
  const p = ip.split(".").map(Number);
  if (p.length !== 4 || p.some(isNaN)) return null;
  let n = ((p[0] << 24) | (p[1] << 16) | (p[2] << 8) | p[3]) >>> 0;
  n = (n + offset) >>> 0;
  const r = `${(n>>>24)&255}.${(n>>>16)&255}.${(n>>>8)&255}.${n&255}`;
  return isPublicIP(r) ? r : null;
}

function prefixToIP(prefix) {
  if (!prefix) return null;
  const base = (typeof prefix === "string" ? prefix : prefix.prefix || "").split("/")[0];
  return addOffset(base, 1);
}

function pickFromPrefixes(prefixes, n) {
  const step = Math.max(1, Math.floor(prefixes.length / n));
  const ips = [];
  for (let i = 0; i < prefixes.length && ips.length < n; i += step) {
    const ip = prefixToIP(prefixes[i]);
    if (ip) ips.push(ip);
  }
  return ips;
}

function pickFromIPs(candidates, n) {
  const step = Math.max(1, Math.floor(candidates.length / n));
  const ips = [];
  for (let i = 0; i < candidates.length && ips.length < n; i += step) {
    const ip = addOffset(candidates[i], 1);
    if (ip) ips.push(ip);
  }
  return ips;
}

function cleanSeed(seed) {
  const out = {};
  for (const [cc, ips] of Object.entries(seed)) {
    if (!cc || cc.length !== 2) continue;
    const valid = (ips || []).filter(isPublicIP);
    if (valid.length > 0) out[cc] = valid;
  }
  return out;
}

function mergeAll(...sources) {
  const merged = {};
  for (const src of sources) {
    for (const [cc, ips] of Object.entries(src)) {
      if (!cc || cc.length !== 2) continue;
      const valid = (ips || []).filter(isPublicIP);
      if (!valid.length) continue;
      merged[cc] = [...new Set([...(merged[cc] || []), ...valid])];
    }
  }
  return merged;
}

function seededShuffle(arr, seed) {
  const a = [...arr];
  let s = seed >>> 0;
  for (let i = a.length - 1; i > 0; i--) {
    s = (Math.imul(s, 1664525) + 1013904223) >>> 0;
    const j = s % (i + 1);
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

function limitAndShuffle(data) {
  const out = {};
  const today = new Date();
  const ds = today.getUTCFullYear() * 10000 + (today.getUTCMonth() + 1) * 100 + today.getUTCDate();
  for (const [cc, ips] of Object.entries(data)) {
    if (!ips?.length) continue;
    const valid = ips.filter(isPublicIP);
    if (!valid.length) continue;
    const seed = ds + cc.charCodeAt(0) * 31 + (cc.charCodeAt(1) || 0) * 17;
    const sh = seededShuffle(valid, seed);
    const take = Math.min(sh.length, Math.max(2, 2 + (seed % 4)));
    out[cc] = sh.slice(0, take);
  }
  return out;
}

function chunk(arr, size) {
  const out = [];
  for (let i = 0; i < arr.length; i += size) out.push(arr.slice(i, i + size));
  return out;
}

