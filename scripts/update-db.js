async function fetchRIPE() {
  const results = {};
  // 分批请求，每批 10 个国家
  const batches = chunk(RIPE_COUNTRIES, 10);
  for (const batch of batches) {
    await Promise.all(batch.map(async (cc) => {
      try {
        const resp = await fetch(
          `https://stat.ripe.net/data/country-resource-list/data.json?resource=${cc}&v=4`,
          {
            signal: AbortSignal.timeout(8000),
            headers: { "Accept": "application/json", "User-Agent": "GH-IPCollector/3.0" },
          }
        );
        const text = await resp.text();
        if (!resp.ok) return;
        const json = JSON.parse(text);
        const prefixes = json?.data?.resources?.ipv4;
        if (!Array.isArray(prefixes) || prefixes.length === 0) return;
        const ips = pickFromPrefixes(prefixes, 5);
        if (ips.length > 0) {
          results[cc] = ips;
          console.log(`[RIPE] ${cc}: ${ips.join(", ")}`);
        }
      } catch (e) {
        // 单国失败不影响整体
      }
    }));
  }
  return results;
}

async function fetchAPNIC() {
  console.log('[APNIC] 开始抓取 delegated-apnic-latest ...');
  const results = {};
  try {
    const resp = await fetch(
      "https://ftp.apnic.net/stats/apnic/delegated-apnic-latest",
      {
        signal: AbortSignal.timeout(30000), // 增加超时到30秒
        headers: { "User-Agent": "GH-IPCollector/3.0" },
      }
    );
    if (!resp.ok) {
      console.log(`[APNIC] 响应状态码: ${resp.status}`);
      return results;
    }

    const text = await resp.text();
    console.log(`[APNIC] 获取到文本长度: ${text.length}`);
    const pool = {};

    for (const line of text.split("\n")) {
      if (line.startsWith("#") || line.startsWith("2") || line.startsWith("apnic")) continue;
      const p = line.split("|");
      if (p.length < 7 || p[2] !== "ipv4") continue;
      const cc = p[1]?.toUpperCase();
      const ip = p[3];
      if (!cc || cc === "*" || !ip) continue;
      if (!pool[cc]) pool[cc] = [];
      if (pool[cc].length < 20) pool[cc].push(ip);
    }

    console.log(`[APNIC] 候选池国家数: ${Object.keys(pool).length}`);

    for (const cc of APNIC_COUNTRIES) {
      const cands = pool[cc];
      if (!cands?.length) continue;
      const ips = pickFromIPs(cands, 5);
      if (ips.length > 0) {
        results[cc] = ips;
        console.log(`[APNIC] ${cc}: ${ips.join(", ")}`);
      }
    }
  } catch (e) {
    console.error("[APNIC] 错误:", e.message);
  }
  return results;
}

